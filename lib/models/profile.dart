class Profile {
  String name;
  String city;
  String age;
  String height;
  String education;
  String occupation;
  String religion;
  String sport;
  String drink;
  String bio;
  String imageUrl;

  Profile({
    required this.name,
    required this.city,
    required this.age,
    required this.height,
    required this.education,
    required this.occupation,
    required this.religion,
    required this.sport,
    required this.drink,
    required this.bio,
    required this.imageUrl,
  });
}
